            <h1 class='pin-it'>Pint it!</h1>
            <p class='pin-it-desc'>How  to  Survive the Japanese  Train System By Sophia  Ysabelle  Yu</p>
            <p class='pin-it-date'>Posted on October 27, 2017</p>
            <div class='row'>
              <div class='col-md-8'>
                <div class='featured-article'>
                  <!--<img src='../img/blogs/featured-1.jpg' class='img-responsive  ' >-->
                  <p class='featured-article-desc'>The  easiest,  cheapest, and most  convenient  way for tourists  to  explore Japan is  by  train.  It’s  also  one of  the most  challenging things  to  conquer in  Japan,  especially  for tourists  visiting  for the first time. With  people  rushing back  and forth and all the different Japanese  signs all around, it  can be  so  overwhelming! It  might seem  confusing at  first,  but keep  calm  and stay  alert because here  are a few tips  to  make  sure  you’ll  survive all the craziness.
                  <br/><br/>
                  1. Check  your  route ahead of  time. Use Google  Maps  or  Navitime  Japan to  search  for your  route as  it  is  updated in  real  time. You can find  the best  routes  to  suit  your  needs and you can plan  ahead so  that  you won’t get too lost  later on. Trains  come  and leave on  time, and if  you’re  on  a time  restraint,  planning  ahead would put you at  a great advantage.  Plus, the estimated cost  of  your  commute would be  displayed.  You can find  Wi-Fi in  most  major stations, but renting or  buying  a data  package would be  better. </p>
                  
                </div>
                <div class='featured-article'>
                  <img src='../img/blogs/featured-1.JPG' class='img-responsive  ' >
                  <p class='featured-article-desc'>2. Breathe.  Don’t stress  yourself  out looking for your  route right away. If  you feel  lost, stand in  an  area  where you won’t block the way and calmly  read  the signs for help. Here’s  where your  powers  of  observation will  kick  in.   
                  <br/><br/>
                  3. Assess where you are right now.  What  station are you in  and where do  you want  to  go? What  line  do  you need  to  get on  and where do  you need  to  get off?  Most  of  the train schedules will  flash in  English and in  Japanese. Maps  are available at  the information booths  and people  will  be  willing to  help  you.    </p>
                  
                </div>
                <div class='featured-article'>
                  <img src='../img/blogs/featured-2.JPG' class='img-responsive  ' >
                  <p class='featured-article-desc'>4. Don’t be  afraid  to  ask for help. Look  for a tourist information center  or  the train station information booth for help. Though  not everyone  you will  encounter will  be  able  to  speak English,  they  will  provide maps  and help  you with  your  route.  Start by  saying  “sumimasen  (excuse me)”  and mention the station or  the line  you want  to  go  to. Don’t forget  to  say “arigatou gozaimasu (thank  you)” once  you’re  done! 
                  <br/><br/>
                  5. Do your  research! It  pays  to  research  a lot about the Japanese  train systems.  There are different train lines and routes  that  are interconnected  with  one another.  Some  stations  are smaller and only  have  one line  passing through,  while others  (like Shinjuku  Station and Tokyo Station)  are connecting  to  almost  all the other lines.    
                  <br/><br/>
                  6. Know your  train lingo.  In  Japan,  there are different kinds of  trains: the local train,  the rapid express train,  the limited express train,  and so  on  and so  forth.  The local train stops on  every single  station,  the rapid express stops on  more  selected  stations, the limited express stops only  on  major stations, and so  on  and so  forth.  Make  sure  you’re  riding  the correct train before  boarding! </p>
                  
                </div>
                <div class='featured-article'>
                  <img src='../img/blogs/featured-3.JPG' class='img-responsive  ' >
                  <p class='featured-article-desc'>7. Invest  in  an  IC  card. To  save  you the time  and the trouble of  having  to  buy train tickets all the time, invest  in  an  IC  card. In  Japan,  the two most  popular IC  cards (to use in  Tokyo)  are the Suica card  and the Pasmo card. Like  the local Beep  card, these IC  cards can be  used  in  buses,  trains, shops,  vending machines, and more! It’s  immensely convenient  and customizable, too!  
                  <br/><br/>
                  8. Keep your  IC  card  or  your  ticket  nearby  for easy  access. You’ll  need  to  tap your  IC  card/ insert  your  ticket  when  you enter and exit  a station.  Keep  it  on  standby when  you’re  about to  enter or  exit  the train so  that  you can enter easily  without having  to  dig in  deep  your  pockets and bags  for your  card  or  ticket  – it  will  also  be  appreciated by  your  fellow  commuters who are rushing by.   
                  <br/><br/>
                  9. Try  to  avoid the rush  hours.  Major stations  in  Tokyo are also  noted as  some  of  the busiest stations  in  the world – and that’s  not an  exaggeration. Some  of  the busiest times would be  from  7 AM  to  8 AM  and 5 PM  to  6 PM  – that’s  when  businessmen would go  and come  back  from  work.   
                  <br/><br/>
                  10. Be  mindful of  yourself. In  Japan,  people  prefer  silence in  their daily commute.  Set your  phone on  silent  or  on  vibrate.  If  you need  to  talk, use your  indoor  voices  and keep  it  brief.  Whenever  you board a train,  let people  exit  the train first before  entering. Lastly, keep  to  the right or  to  the left  (depending  where you are in  Japan)  on  the escalator to  let people  pass  by. </p>
                  
                </div>
                <div class='featured-article'>
                  <img src='../img/blogs/featured-4.JPG' class='img-responsive  ' >
                  <p class='featured-article-desc'>That’s it! Just  remember  these 10  tips  when  you’re  in  the station and you’ll  be  fine. It’s  confusing at  first,  but don’t be  afraid. Once  you get the hang  of  it, you’ll  find  it  to  be  extremely easy  and convenient! Good  luck! Ganbatte! </p>
                  
                </div>
